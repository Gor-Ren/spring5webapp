package guru.springframework.spring5webapp.controllers;

import guru.springframework.spring5webapp.repositories.AuthorRepository;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;

@Controller
public class AuthorController {

    private static final String AUTHORS_ATTRIBUTE_NAME = "authors";
    private static final String AUTHORS_VIEW_NAME = "authors";
    private AuthorRepository authorRepository;

    public AuthorController(AuthorRepository authorRepository) {
        this.authorRepository = authorRepository;
    }

    @RequestMapping("/authors")
    public String getAuthors(Model model) {
        model.addAttribute(AUTHORS_ATTRIBUTE_NAME, authorRepository.findAll());
        return AUTHORS_VIEW_NAME;
    }
}
